function setup(){
    createCanvas(800, 800);
    background(0);

}

function draw(){
    fill(255,0,0);
    rect(400,400,100,100);
    fill(0,0,255);
    ellipse(100,100,30,80);
    fill(200,255,200);
    ellipse(600,600,120,120);
    fill(100,255,100);
    ellipse(600,600,80,80);
    fill(0,255,0);
    ellipse(600,600,40,40);

}